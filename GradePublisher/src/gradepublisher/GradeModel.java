package gradepublisher;

public class GradeModel {

	private String grade;

	//default constructor
	public GradeModel() {
		
	}

	//overloaded constructor
	public GradeModel(String grade) {
		super();
		this.grade = grade;
	}

	//getters and setters
	public String getGrade() {
		return grade;
	}

	public void setGrade(String grade) {
		this.grade = grade;
	}
	
	
	
	
	
	
}
